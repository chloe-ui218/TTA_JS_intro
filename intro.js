let width = 4;
let height = 5;

console.log('width: ${width} cm');
console.log('height: ${height} cm');

// area = width * height
let area = width * height;
console.log('Area: ${area} cm2' );

// perimeter = 2 * (width + height)
let perimeter = 2 * (width + height);
console.log('Perimeter: ${perimeter} cm');